/** The brand mark for a member's local name, or `null` if it names no known tool. */
export function agentLogo(name: string | null | undefined): AgentLogo | null {
  const n = name?.trim();
  if (!n) return null;
  return AGENT_LOGOS.find((a) => a.match.test(n))?.logo ?? null;
}
